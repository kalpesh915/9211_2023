#include<stdio.h>
#include<math.h>

void main(){
    printf("\n FABS is %lf ", fabs(-123.45));
    printf("\n CEIL is %lf ", ceil(123.45));
    printf("\n FLOOR is %lf ", floor(123.45));
    printf("\n SQRT is %lf ", sqrt(144));
    printf("\n POW is %lf ", pow(2, 15));
    printf("\n FMOD is %lf ", fmod(123, 8));
    printf("\n COS is %lf ", cos(0.13));
}
